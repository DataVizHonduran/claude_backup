def fmt_ret(val: float | None, with_sign: bool = True) -> tuple[str, str]:
    if val is None:
        return "n/a", "#6b7280"
    sign = "+" if (with_sign and val >= 0) else ""
    color = "#16a34a" if val >= 0 else "#dc2626"
    return f"{sign}{val:.2f}%", color



def build_quadrant_context(holdings: list[dict], momentum: dict, fundamentals: dict) -> str:
    short_data = fundamentals.get("short", {})
    pe_data = fundamentals.get("pe", {})

    pts = [(h["ticker"], h["name"], h["weight"], momentum.get(h["ticker"]))
           for h in holdings if h["ticker"] in momentum]

    shorts = [short_data.get(p[0]) for p in pts]
    valid_si = [(i, v) for i, v in enumerate(shorts) if v is not None]
    valid_si_sorted = sorted(valid_si, key=lambda x: x[1])
    n_si = len(valid_si_sorted)
    idx_to_si_decile = {i: min(10, int(rank * 10 / n_si) + 1)
                        for rank, (i, _) in enumerate(valid_si_sorted)}

    import statistics as _st
    returns = [p[3] for p in pts]
    order = sorted(range(len(returns)), key=lambda i: returns[i])
    ranks = [0] * len(returns)
    for r, i in enumerate(order):
        ranks[i] = r + 1
    m, s = _st.mean(ranks), _st.stdev(ranks) or 1
    zx = [(r - m) / s for r in ranks]

    quadrants = {"Squeeze Candidates": [], "Battleground": [],
                 "Clean Longs": [], "Quiet Laggards": []}
    for i, (tk, nm, wt, ret) in enumerate(pts):
        si_d = idx_to_si_decile.get(i, 5)
        si_pct = shorts[i]
        pe = pe_data.get(tk)
        high_mom = zx[i] > 0
        high_si = si_d > 5
        label = ("Squeeze Candidates" if high_mom and high_si else
                 "Battleground" if not high_mom and high_si else
                 "Clean Longs" if high_mom and not high_si else
                 "Quiet Laggards")
        pe_str = f"fwd P/E {pe:.1f}" if pe else "P/E n/a"
        quadrants[label].append(
            f"{tk} ({nm}): 3M mom {ret:+.1f}%, SI decile {si_d} ({si_pct:.1f}% float), {pe_str}, weight {wt:.2f}%"
        )

    lines = ["GVIP Quadrant Positioning:"]
    for q, items in quadrants.items():
        lines.append(f"\n{q} ({len(items)} names):")
        lines.extend(f"  {item}" for item in items)
    return "\n".join(lines)



def add_landing_card():
    if not LANDING_PAGE.exists():
        return
    content = LANDING_PAGE.read_text()
    if "reports/gvip" in content:
        return
    marker = '            </div>\n        </section>\n    </main>'
    if marker not in content:
        print("  ⚠ Could not find landing page insertion point; skipping card addition")
        return
    card_indented = '\n'.join('                ' + line if line.strip() else line for line in LANDING_CARD.strip().split('\n'))
    updated = content.replace(marker, f'{card_indented}\n            </div>\n        </section>\n    </main>', 1)
    LANDING_PAGE.write_text(updated)
    print("  ✓ Added landing card to index.html")


def publish(today: str, added_landing_card: bool):
    repo = Path.home() / "boquin.github.io"
    files = [str(REPORT_DIR / "index.html")]
    if added_landing_card:
        files.append(str(LANDING_PAGE))

    cmds = [
        ["git", "-C", str(repo), "pull"],
        ["git", "-C", str(repo), "add"] + files,
        ["git", "-C", str(repo), "commit", "-m", f"Update GVIP Holdings Monitor — {today}"],
        ["git", "-C", str(repo), "pull", "--rebase"],
        ["git", "-C", str(repo), "push"],
    ]
    for cmd in cmds:
        result = subprocess.run(cmd, capture_output=True, text=True)
        if result.returncode != 0:
            print(f"  ⚠ git error: {result.stderr.strip()}", file=sys.stderr)
        else:
            print(f"  ✓ {' '.join(cmd[3:])}")


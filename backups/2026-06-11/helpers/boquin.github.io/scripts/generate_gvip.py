def build_context(today_data: dict, diff: dict) -> str:
    perf = today_data["performance"]
    holdings = today_data["holdings"]
    sectors = today_data["sectors"]

    lines = [
        f"Date: {today_data['date']}",
        f"AUM: ${perf['aum']/1e6:.1f}M | Price: ${perf['price']:.2f}",
        "Returns: " + "  ".join(
            f"{label} {(v):+.2f}%" if v is not None else f"{label} n/a"
            for label, v in [("1M", perf["ret_1m"]), ("3M", perf["ret_3m"]),
                              ("YTD", perf["ret_ytd"]), ("1Y", perf["ret_1y"])]
        ),
        "",
        "Top 15 Holdings (by weight):",
    ]
    for h in holdings[:15]:
        lines.append(f"  {h['ticker']:8s}  {h['weight']:+.2f}%  {h['name']}")

    lines.append("")
    lines.append("Sector Allocation:")
    for sector, pct in sorted(sectors.items(), key=lambda x: -x[1]):
        label = SECTOR_LABELS.get(sector, sector)
        lines.append(f"  {label}: {pct:.1f}%")

    if diff.get("new"):
        lines.append("")
        lines.append("New positions: " + ", ".join(f"{h['ticker']} ({h['weight']:+.1f}%)" for h in diff["new"]))
    if diff.get("removed"):
        lines.append("Removed: " + ", ".join(h["ticker"] for h in diff["removed"]))
    if diff.get("changed"):
        lines.append("Weight changes: " + ", ".join(
            f"{c['ticker']} {c['prior']:.1f}%→{c['today']:.1f}% ({c['delta']:+.1f}%)"
            for c in sorted(diff["changed"], key=lambda x: -abs(x["delta"]))[:8]
        ))

    return "\n".join(lines)



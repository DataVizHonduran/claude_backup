def change_log_html(diff: dict, prior_date: str | None) -> str:
    if not prior_date:
        return '<p style="color:#6b7280;font-style:italic">First snapshot — no prior data to compare.</p>'

    rows = []
    for h in diff.get("new", []):
        rows.append(f'<tr><td style="color:#16a34a">New</td><td style="font-family:monospace">{h["ticker"]}</td><td>{h["name"]}</td><td style="text-align:right;color:#16a34a">+{h["weight"]:.2f}%</td></tr>')
    for h in diff.get("removed", []):
        rows.append(f'<tr><td style="color:#dc2626">Removed</td><td style="font-family:monospace">{h["ticker"]}</td><td>{h["name"]}</td><td style="text-align:right;color:#dc2626">—</td></tr>')
    for c in sorted(diff.get("changed", []), key=lambda x: -abs(x["delta"])):
        sign = "+" if c["delta"] > 0 else ""
        color = "#16a34a" if c["delta"] > 0 else "#dc2626"
        rows.append(f'<tr><td style="color:{color}">Changed</td><td style="font-family:monospace">{c["ticker"]}</td><td>{c["name"]}</td><td style="text-align:right;color:{color}">{sign}{c["delta"]:.2f}%</td></tr>')

    if not rows:
        return f'<p style="color:#6b7280;font-style:italic">No significant changes vs prior snapshot ({prior_date}).</p>'

    return (
        f'<p style="color:#6b7280;font-size:0.85rem">vs {prior_date}</p>'
        '<div style="overflow-x:auto"><table class="htable">'
        '<thead><tr><th>Change</th><th>Ticker</th><th>Company</th><th style="text-align:right">Δ Weight</th></tr></thead>'
        f'<tbody>{"".join(rows)}</tbody></table></div>'
    )



def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--cache-only", action="store_true")
    parser.add_argument("--no-publish", action="store_true")
    parser.add_argument("--print-context", action="store_true")
    parser.add_argument("--summary-file", type=str)
    parser.add_argument("--quadrant-file", type=str)
    args = parser.parse_args()

    today = date.today().strftime("%Y-%m-%d")
    today_path = snapshot_path(today)

    # Step 1: Load or fetch data
    if args.cache_only or today_path.exists():
        if today_path.exists():
            print(f"Loading today's snapshot: {today_path.name}", file=sys.stderr)
            with open(today_path) as f:
                today_data = json.load(f)
        else:
            files = sorted(CACHE_DIR.glob("*.json"))
            if not files:
                print("ERROR: No cached snapshots found.", file=sys.stderr)
                sys.exit(1)
            print(f"Cache-only mode: loading {files[-1].name}", file=sys.stderr)
            with open(files[-1]) as f:
                today_data = json.load(f)
            today = today_data["date"]
    else:
        print("Fetching GVIP data...")
        holdings = fetch_holdings()
        print("Fetching performance data from yfinance...")
        performance = fetch_performance()
        print("Fetching sector weights...")
        sectors = fetch_sector_weights()

        today_data = {
            "date": today,
            "holdings_as_of": today,
            "holdings": holdings,
            "performance": performance,
            "sectors": sectors,
        }
        save_snapshot(today_data, today)

    # Step 2: Prior snapshot for diff (~2 weeks ago)
    prior_snapshot = snapshot_two_weeks_ago(today)
    prior_date = prior_snapshot["date"] if prior_snapshot else None

    diff = {}
    if prior_snapshot:
        diff = compute_diff(today_data["holdings"], prior_snapshot.get("holdings", []))

    # --print-context
    if args.print_context:
        print_context_for_claude(today_data, diff, prior_date)
        return

    # Step 3: Summary
    summary = ""
    fresh_summary = False
    if args.summary_file:
        summary = Path(args.summary_file).read_text().strip()
        print(f"  ✓ Summary loaded from {args.summary_file} ({len(summary.split())} words)")
        fresh_summary = True
    else:
        hf_token = os.environ.get("HF_TOKEN")
        if hf_token and should_refresh_summary():
            meta = load_summary_meta()
            last_date = meta.get("generated_at", "never")
            print(f"  ↻ Commentary stale (last: {last_date}), refreshing via Gemma 4...")
            context_text = build_context(today_data, diff)
            try:
                summary = generate_summary_gemma(context_text, hf_token)
                save_summary_meta(datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC"), len(summary.split()))
                print(f"  ✓ Gemma summary generated ({len(summary.split())} words)")
                fresh_summary = True
            except Exception as e:
                print(f"  WARNING: Gemma generation failed ({e}), falling back to existing",
                      file=sys.stderr)
                summary = extract_existing_summary()
        else:
            summary = extract_existing_summary()
            if summary:
                print(f"  ✓ Preserved existing summary ({len(summary.split())} words)")

    # Step 4: Price history + momentum + render
    print("Fetching 1-year price history (GVIP + SPY)...")
    price_history = fetch_price_history()
    print("Fetching 3-month momentum for holdings...")
    momentum = fetch_holding_momentum(today_data["holdings"])
    print(f"  ✓ Momentum data for {len(momentum)} tickers")
    print("Fetching fundamentals (fwd P/E + short interest) for holdings...")
    holding_tickers = [h["ticker"] for h in today_data["holdings"]]
    fundamentals = fetch_holding_fundamentals(holding_tickers)
    valid_pe = sum(1 for v in fundamentals["pe"].values() if v is not None)
    valid_si = sum(1 for v in fundamentals["short"].values() if v is not None)
    print(f"  ✓ Fwd P/E: {valid_pe}/{len(holding_tickers)}  Short interest: {valid_si}/{len(holding_tickers)}")
    print("Fetching MTUM holdings...")
    try:
        mtum_holdings = fetch_mtum_holdings()
    except Exception as e:
        print(f"  ⚠ MTUM fetch failed ({e}), skipping relative positioning chart", file=sys.stderr)
        mtum_holdings = {}
    generated_at = datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC") if fresh_summary else None

    # Step 4b: Quadrant commentary
    quadrant_commentary = ""
    if args.quadrant_file:
        quadrant_commentary = Path(args.quadrant_file).read_text().strip()
        save_quadrant_meta(datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC"), len(quadrant_commentary.split()))
        print(f"  ✓ Quadrant commentary loaded from {args.quadrant_file} ({len(quadrant_commentary.split())} words)")
    hf_token = os.environ.get("HF_TOKEN")
    if not quadrant_commentary and hf_token and should_refresh_quadrant_commentary():
        print("  ↻ Quadrant commentary stale, generating via Gemma 4...")
        try:
            q_context = build_quadrant_context(today_data["holdings"], momentum, fundamentals)
            quadrant_commentary = generate_quadrant_commentary_gemma(q_context, hf_token)
            save_quadrant_meta(datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC"), len(quadrant_commentary.split()))
            print(f"  ✓ Quadrant commentary generated ({len(quadrant_commentary.split())} words)")
        except Exception as e:
            print(f"  WARNING: Quadrant commentary failed ({e}), falling back to existing", file=sys.stderr)
            quadrant_commentary = extract_existing_quadrant_commentary()
    elif not quadrant_commentary:
        quadrant_commentary = extract_existing_quadrant_commentary()
        if quadrant_commentary:
            print(f"  ✓ Preserved existing quadrant commentary ({len(quadrant_commentary.split())} words)")

    snapshots = load_snapshots()

    REPORT_DIR.mkdir(parents=True, exist_ok=True)
    html = render_html(today_data, diff, summary, snapshots, prior_date, price_history, momentum, fundamentals, quadrant_commentary, generated_at, mtum_holdings)
    out_path = REPORT_DIR / "index.html"
    out_path.write_text(html)
    print(f"  ✓ Report written: {out_path}")

    # Step 5: Publish
    if not args.no_publish:
        added_card = False
        if LANDING_PAGE.exists():
            content = LANDING_PAGE.read_text()
            if "reports/gvip" not in content:
                add_landing_card()
                added_card = True
        print("Publishing to boquin.github.io...")
        publish(today, added_card)
        print(f"  ✓ Published: https://boquin.xyz/reports/gvip/")
    else:
        print("Skipping publish (--no-publish).")


if __name__ == "__main__":
    main()

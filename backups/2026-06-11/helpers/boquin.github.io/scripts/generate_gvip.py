def quadrant_chart_js(holdings: list[dict], momentum: dict, fundamentals: dict) -> str:
    import math, statistics

    pe_data = fundamentals.get("pe", {})
    short_data = fundamentals.get("short", {})

    pts = [(h["ticker"], h["name"], h["weight"], momentum[h["ticker"]])
           for h in holdings if h["ticker"] in momentum]
    if len(pts) < 3:
        return '<p style="color:#6b7280;font-style:italic">Insufficient momentum data for quadrant chart.</p>'

    tickers = [p[0] for p in pts]
    names   = [p[1] for p in pts]
    weights = [p[2] for p in pts]
    returns = [p[3] for p in pts]
    shorts  = [short_data.get(tk) for tk in tickers]

    def rank_zscore(vals):
        """Rank-based Z-scores: forces uniform spread regardless of distribution."""
        order = sorted(range(len(vals)), key=lambda i: vals[i])
        ranks = [0] * len(vals)
        for r, i in enumerate(order):
            ranks[i] = r + 1
        m = statistics.mean(ranks)
        s = statistics.stdev(ranks) or 1
        return [(r - m) / s for r in ranks]

    zx = rank_zscore(returns)
    valid_shorts = [v for v in shorts if v is not None]

    # assign short interest deciles 1–10
    si_with_idx = sorted([(i, v) for i, v in enumerate(shorts) if v is not None], key=lambda x: x[1])
    n_si = len(si_with_idx)
    idx_to_si_decile = {i: min(10, int(rank * 10 / n_si) + 1) for rank, (i, _) in enumerate(si_with_idx)}
    zy = [idx_to_si_decile.get(i, 5) for i in range(len(shorts))]
    si_cap = None  # no winsorization needed

    # P/E color: assign deciles 1–10 across valid PEs, gray for missing
    pe_vals = [pe_data.get(tk) for tk in tickers]
    valid_with_idx = [(i, pe_vals[i]) for i in range(len(pe_vals))
                      if pe_vals[i] is not None and not math.isnan(pe_vals[i])]
    valid_with_idx.sort(key=lambda x: x[1])
    n_valid = len(valid_with_idx)
    idx_to_decile = {
        i: min(10, int(rank * 10 / n_valid) + 1)
        for rank, (i, _) in enumerate(valid_with_idx)
    }

    def decile_color(d):
        norm = (d - 1) / 9.0  # 0 (cheapest) → 1 (priciest)
        if norm < 0.5:
            return f"rgb({int(norm * 2 * 220)},200,60)"
        else:
            return f"rgb(220,{int((1 - norm) * 2 * 200)},60)"

    colors = [
        decile_color(idx_to_decile[i]) if i in idx_to_decile else "#9ca3af"
        for i in range(len(pe_vals))
    ]
    deciles = [idx_to_decile.get(i) for i in range(len(pe_vals))]

    def si_label(si):
        if si is None:
            return "n/a"
        clipped = si_cap is not None and si > si_cap
        return f"{si:.1f}%{'*' if clipped else ''}"

    hover = [
        f"<b>{tk}</b><br>{nm}<br>Momentum (3M): {ret:+.1f}%<br>"
        f"Short % Float: {si_label(si)}<br>"
        f"Fwd P/E: {f'{pe:.1f} (D{d})' if pe is not None and not math.isnan(pe) else 'n/a'}<extra></extra>"
        for tk, nm, wt, ret, si, pe, d in zip(tickers, names, weights, returns, shorts, pe_vals, deciles)
    ]

    axis_pad = 2.2
    si_mid = 5.5  # decile midpoint crosshair
    si_max = 10.5
    si_min = 0.5
    max_weight = max(weights) if weights else 1
    sizes = [6 + (w / max_weight) * 14 for w in weights]

    trace = {
        "type": "scatter",
        "mode": "markers+text",
        "x": zx,
        "y": zy,
        "text": tickers,
        "textposition": "top center",
        "textfont": {"size": 9, "color": "#374151"},
        "hovertemplate": hover,
        "marker": {
            "size": sizes,
            "sizemode": "diameter",
            "color": colors,
            "opacity": 0.85,
            "line": {"width": 1.5, "color": "#fff"},
        },
    }

    layout = {
        "paper_bgcolor": "#fff",
        "plot_bgcolor": "#f8fafc",
        "margin": {"l": 60, "r": 40, "t": 30, "b": 60},
        "height": 560,
        "xaxis": {
            "title": "Relative Momentum (Z-Score)",
            "zeroline": False,
            "range": [-axis_pad, axis_pad],
            "gridcolor": "#e5e7eb",
        },
        "yaxis": {
            "title": "Short Interest Decile (1 = lowest, 10 = highest)",
            "zeroline": False,
            "range": [si_min, si_max],
            "tickvals": list(range(1, 11)),
            "gridcolor": "#e5e7eb",
        },
        "shapes": [
            {"type": "line", "x0": -axis_pad, "x1": axis_pad,
             "y0": si_mid, "y1": si_mid,
             "line": {"color": "#9ca3af", "width": 1, "dash": "dash"}},
            {"type": "line", "x0": 0, "x1": 0, "y0": si_min, "y1": si_max,
             "line": {"color": "#9ca3af", "width": 1, "dash": "dash"}},
        ],
        "annotations": [
            {"x": axis_pad - 0.05, "y": 10.2, "xanchor": "right", "yanchor": "top",
             "text": "Squeeze Candidates", "showarrow": False,
             "font": {"size": 10, "color": "#6b7280"}, "bgcolor": "rgba(255,255,255,0.7)"},
            {"x": -axis_pad + 0.05, "y": 10.2, "xanchor": "left", "yanchor": "top",
             "text": "Battleground", "showarrow": False,
             "font": {"size": 10, "color": "#6b7280"}, "bgcolor": "rgba(255,255,255,0.7)"},
            {"x": axis_pad - 0.05, "y": 0.8, "xanchor": "right", "yanchor": "bottom",
             "text": "Clean Longs", "showarrow": False,
             "font": {"size": 10, "color": "#6b7280"}, "bgcolor": "rgba(255,255,255,0.7)"},
            {"x": -axis_pad + 0.05, "y": 0.8, "xanchor": "left", "yanchor": "bottom",
             "text": "Quiet Laggards", "showarrow": False,
             "font": {"size": 10, "color": "#6b7280"}, "bgcolor": "rgba(255,255,255,0.7)"},
        ],
    }

    pe_legend = (
        '<p style="font-size:0.78rem;color:#6b7280;text-align:center;margin-top:-1rem;margin-bottom:1rem">'
        'Dot color = forward P/E decile &nbsp;|&nbsp; '
        '<span style="display:inline-block;width:10px;height:10px;background:rgb(0,200,60);border-radius:50%;vertical-align:middle"></span> D1 cheapest '
        '&nbsp;→&nbsp; '
        '<span style="display:inline-block;width:10px;height:10px;background:rgb(220,0,60);border-radius:50%;vertical-align:middle"></span> D10 priciest '
        '&nbsp;|&nbsp; <span style="color:#9ca3af">●</span> n/a'
        '&nbsp;|&nbsp; Bubble size = ETF weight %'
        '</p>'
    )

    return f"""
<div id="quadrantChart" style="max-width:700px;margin:0 auto 1rem;"></div>
{pe_legend}
<script>
Plotly.newPlot('quadrantChart', [{json.dumps(trace)}], {json.dumps(layout)}, {{responsive: true, displayModeBar: false}});
</script>"""



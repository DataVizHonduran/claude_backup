def generate_summary_gemma(context_text: str, hf_token: str, retries: int = 5) -> str:
    client = InferenceClient(model=MODEL_ID, token=hf_token, timeout=300)
    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": (
            "Please analyse the following GVIP positioning snapshot and write your commentary:\n\n"
            + context_text
        )},
    ]
    for attempt in range(retries):
        try:
            response = client.chat.completions.create(
                messages=messages,
                temperature=0.3,
                max_tokens=600,
                stream=False,
            )
            text = response.choices[0].message.content or ""
            if not text.strip():
                raise ValueError("Gemma returned empty response")
            return text.strip()
        except Exception as e:
            is_rate_limit = "429" in str(e) or "Too Many Requests" in str(e)
            if attempt < retries - 1:
                wait = 60 * (attempt + 1) if is_rate_limit else 10
                print(f"  Gemma attempt {attempt+1}/{retries} failed ({e}), retrying in {wait}s...",
                      file=sys.stderr)
                time.sleep(wait)
            else:
                raise



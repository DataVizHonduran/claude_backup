def fetch_performance() -> dict:
    """Scrape 1MO and YTD returns (MKT price) from each ETF page on unlimitedetfs.com."""
    perf = {}
    headers = {"User-Agent": "unlimited-etfs-monitor research@claude.local"}
    for ticker in ETF_TICKERS:
        try:
            resp = requests.get(ETF_PAGES[ticker], timeout=15, headers=headers)
            resp.raise_for_status()
            soup = BeautifulSoup(resp.text, "html.parser")

            # Find the wpDataTable that has performance columns (1MO, YTD)
            table = None
            for t in soup.find_all("table", class_="wpDataTable"):
                thead = t.find("thead")
                if thead and "1MO" in thead.get_text().upper():
                    table = t
                    break
            if not table:
                raise ValueError("performance wpDataTable not found")

            # Parse column headers
            headers_row = table.find("thead").find_all("th")
            cols = [th.get_text(strip=True).upper() for th in headers_row]

            # Find the MKT row (first tbody row)
            rows = table.find("tbody").find_all("tr")
            mkt_row = next(
                (r for r in rows if "MKT" in r.get_text().upper()),
                rows[0]
            )
            cells = [td.get_text(strip=True) for td in mkt_row.find_all("td")]

            def get_val(col_name):
                if col_name in cols:
                    idx = cols.index(col_name)
                    if idx < len(cells):
                        return cells[idx].replace("%", "").strip()
                return None

            def parse_pct(s):
                try:
                    return float(s) if s else None
                except (ValueError, TypeError):
                    return None

            # Find the "as of" date — look for text like "as of MM/DD/YYYY"
            as_of = None
            for tag in soup.find_all(string=lambda t: t and "as of" in t.lower()):
                import re
                m = re.search(r"\d{2}/\d{2}/\d{4}", tag)
                if m:
                    as_of = m.group()
                    break

            perf[ticker] = {
                "ret_1m": parse_pct(get_val("1MO")),
                "ret_ytd": parse_pct(get_val("YTD")),
                "ret_3m": parse_pct(get_val("3MO")),
                "as_of": as_of,
            }
        except Exception as e:
            print(f"  ⚠ Performance fetch failed for {ticker}: {e}", file=sys.stderr)
            perf[ticker] = {"ret_1m": None, "ret_ytd": None, "ret_3m": None, "as_of": None}
    return perf


def fmt_ret(val: float | None) -> tuple[str, str]:
    """Return (formatted string, color) for a return value."""
    if val is None:
        return "n/a", "#6b7280"
    sign = "+" if val >= 0 else ""
    color = "#16a34a" if val >= 0 else "#dc2626"
    return f"{sign}{val:.1f}%", color



def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--cache-only", action="store_true")
    parser.add_argument("--no-publish", action="store_true")
    parser.add_argument("--print-context", action="store_true", help="Print JSON context for Claude and exit")
    parser.add_argument("--summary-file", type=str, help="Path to file containing the summary text")
    args = parser.parse_args()

    today = date.today().strftime("%Y-%m-%d")
    today_path = snapshot_path(today)

    # Step 1: Load or fetch data
    if args.cache_only or today_path.exists():
        if today_path.exists():
            print(f"Loading today's snapshot from cache: {today_path.name}")
            with open(today_path) as f:
                today_data = json.load(f)
        else:
            # cache-only but no today snapshot — use latest
            files = sorted(CACHE_DIR.glob("*.json"))
            if not files:
                print("ERROR: No cached snapshots found.", file=sys.stderr)
                sys.exit(1)
            print(f"Cache-only mode: loading {files[-1].name}")
            with open(files[-1]) as f:
                today_data = json.load(f)
            today = today_data["date"]
    else:
        print("Fetching holdings CSVs...")
        etf_data = {}
        for ticker in ETF_TICKERS:
            rows = fetch_csv(ticker)
            if rows is None:
                # Fall back to latest cache for this ETF
                files = sorted(CACHE_DIR.glob("*.json"))
                for cf in reversed(files):
                    with open(cf) as fh:
                        cached = json.load(fh)
                    if ticker in cached.get("etfs", {}):
                        etf_data[ticker] = cached["etfs"][ticker]
                        print(f"  ↩ {ticker}: using cached data from {cached['date']}")
                        break
                else:
                    etf_data[ticker] = []
            else:
                etf_data[ticker] = rows

        today_data = {"date": today, "etfs": etf_data}
        save_snapshot(today_data, today)

    # Step 2: Load prior snapshot for diff
    prior_snapshot = latest_snapshot_before(today)
    prior_date = prior_snapshot["date"] if prior_snapshot else None

    # Step 3: Compute metrics and diffs
    metrics = {ticker: compute_etf_metrics(today_data["etfs"][ticker]) for ticker in ETF_TICKERS}
    diffs = {}
    if prior_snapshot:
        for ticker in ETF_TICKERS:
            diffs[ticker] = compute_diff(
                today_data["etfs"][ticker],
                prior_snapshot["etfs"].get(ticker, [])
            )

    # --print-context mode: emit JSON for Claude to summarize, then exit
    if args.print_context:
        print_context_for_claude(today_data, metrics, diffs, prior_date)
        return

    # Step 4: Load summary from file (written by Claude), auto-refresh via Gemma, or preserve
    summary = ""
    fresh_summary = False
    if args.summary_file:
        summary = Path(args.summary_file).read_text().strip()
        print(f"  ✓ Summary loaded from {args.summary_file} ({len(summary.split())} words)")
        fresh_summary = True
    else:
        hf_token = os.environ.get("HF_TOKEN")
        if hf_token and should_refresh_summary():
            meta = load_summary_meta()
            last_date = meta.get("generated_at", "never")
            print(f"  ↻ Commentary stale (last: {last_date}), refreshing via Gemma 4...")
            context_text = build_gemma_context(today_data, metrics, window_days=SUMMARY_REFRESH_DAYS)
            try:
                summary = generate_summary_gemma(context_text, hf_token)
                save_summary_meta(datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC"), len(summary.split()))
                print(f"  ✓ Gemma summary generated ({len(summary.split())} words)")
                fresh_summary = True
            except Exception as e:
                print(f"  WARNING: Gemma generation failed ({e}), falling back to existing summary",
                      file=sys.stderr)
                summary = extract_existing_summary()
        else:
            summary = extract_existing_summary()
            if summary:
                print(f"  ✓ Preserved existing macro summary ({len(summary.split())} words)")
            else:
                print("  ℹ No existing summary to preserve")

    # Step 5: Fetch performance and render HTML
    print("Fetching ETF performance...")
    performance = fetch_performance()
    for ticker, p in performance.items():
        if p["ret_1m"] is not None:
            r1, _ = fmt_ret(p["ret_1m"])
            r3, _ = fmt_ret(p["ret_3m"])
            ry, _ = fmt_ret(p["ret_ytd"])
            print(f"  {ticker}: 1M {r1}  3M {r3}  YTD {ry}  (as of {p['as_of']})")

    generated_at = datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC") if fresh_summary else None
    all_snapshots = load_snapshots()
    html = render_html(today_data, metrics, diffs, summary, all_snapshots, prior_date, performance, generated_at)
    REPORT_DIR.mkdir(parents=True, exist_ok=True)
    out_path = REPORT_DIR / "index.html"
    out_path.write_text(html)
    print(f"  ✓ Report written: {out_path}")

    # Step 6: Publish
    if not args.no_publish:
        added_card = False
        if LANDING_PAGE.exists():
            content = LANDING_PAGE.read_text()
            if "unlimited-etfs" not in content:
                add_landing_card()
                added_card = True
        print("Publishing to boquin.github.io...")
        publish(today, added_card)
        print(f"  ✓ Published: https://boquin.xyz/reports/unlimited-etfs/")
    else:
        print("Skipping publish (--no-publish).")


if __name__ == "__main__":
    main()

def fetch_csv(ticker: str) -> list[dict] | None:
    url = CSV_URL.format(ticker=ticker)
    try:
        resp = requests.get(url, timeout=15, headers={"User-Agent": "unlimited-etfs-monitor research@claude.local"})
        resp.raise_for_status()
        reader = csv.DictReader(io.StringIO(resp.text))
        rows = []
        for row in reader:
            rows.append(parse_row(row))
        print(f"  ✓ {ticker}: {len(rows)} positions fetched")
        return rows
    except Exception as e:
        print(f"  ✗ {ticker}: fetch failed ({e})", file=sys.stderr)
        return None


def parse_row(row: dict) -> dict:
    def safe_float(val, default=0.0):
        try:
            return float(str(val).replace(",", "").replace("%", "").strip())
        except (ValueError, TypeError):
            return default

    weight_str = row.get("Weightings", "0").replace("%", "").strip()
    return {
        "StockTicker": row.get("StockTicker", "").strip(),
        "SecurityName": row.get("SecurityName", "").strip(),
        "Shares": safe_float(row.get("Shares", 0)),
        "Price": safe_float(row.get("Price", 0)),
        "MarketValue": safe_float(row.get("MarketValue", 0)),
        "Weightings": safe_float(weight_str),
        "NetAssets": safe_float(row.get("NetAssets", 0)),
    }

# ── Cache ─────────────────────────────────────────────────────────────────────

def snapshot_path(d: str) -> Path:
    return CACHE_DIR / f"{d}.json"


def save_snapshot(data: dict, today: str):
    path = snapshot_path(today)
    with open(path, "w") as f:
        json.dump(data, f, indent=2)
    print(f"  Saved snapshot: {path.name}")
    prune_snapshots()


def prune_snapshots():
    files = sorted(CACHE_DIR.glob("*.json"))
    while len(files) > MAX_SNAPSHOTS:
        files[0].unlink()
        print(f"  Pruned old snapshot: {files[0].name}")
        files = files[1:]


def load_snapshots() -> list[dict]:
    """Load all cached snapshots sorted oldest→newest."""
    files = sorted(CACHE_DIR.glob("*.json"))
    snapshots = []
    for f in files:
        with open(f) as fh:
            snapshots.append(json.load(fh))
    return snapshots


def latest_snapshot_before(today: str) -> dict | None:
    files = sorted(CACHE_DIR.glob("*.json"))
    candidates = [f for f in files if f.stem < today]
    if not candidates:
        return None
    with open(candidates[-1]) as fh:
        return json.load(fh)

# ── Metrics ───────────────────────────────────────────────────────────────────

CASH_KEYWORDS = {"cash", "money market", "government obligations", "treasury", "fgxxx", "cash&other"}

def is_cash(row: dict) -> bool:
    name_lower = row["SecurityName"].lower()
    ticker_lower = row["StockTicker"].lower()
    return any(kw in name_lower or kw in ticker_lower for kw in CASH_KEYWORDS)


def compute_etf_metrics(holdings: list[dict]) -> dict:
    net_assets = holdings[0]["NetAssets"] if holdings else 0
    longs = [h for h in holdings if h["Weightings"] > 0 and not is_cash(h)]
    shorts = [h for h in holdings if h["Weightings"] < 0]
    cash_rows = [h for h in holdings if is_cash(h)]

    gross_long = sum(h["Weightings"] for h in longs)
    gross_short = abs(sum(h["Weightings"] for h in shorts))
    net_exposure = gross_long - gross_short
    cash_pct = sum(h["Weightings"] for h in cash_rows)

    top5 = sorted(holdings, key=lambda h: abs(h["Weightings"]), reverse=True)[:5]

    return {
        "net_assets": net_assets,
        "gross_long": gross_long,
        "gross_short": gross_short,
        "net_exposure": net_exposure,
        "cash_pct": cash_pct,
        "top5": top5,
        "n_longs": len(longs),
        "n_shorts": len(shorts),
    }


def compute_diff(today_holdings: list[dict], prior_holdings: list[dict]) -> dict:
    today_map = {h["StockTicker"]: h for h in today_holdings}
    prior_map = {h["StockTicker"]: h for h in prior_holdings}

    new_positions = [today_map[t] for t in today_map if t not in prior_map and t not in ("Cash&Other",)]
    removed = [prior_map[t] for t in prior_map if t not in today_map and t not in ("Cash&Other",)]
    changed = []
    for ticker in today_map:
        if ticker in prior_map:
            delta = today_map[ticker]["Weightings"] - prior_map[ticker]["Weightings"]
            if abs(delta) >= 1.5:
                changed.append({
                    "ticker": ticker,
                    "name": today_map[ticker]["SecurityName"],
                    "today": today_map[ticker]["Weightings"],
                    "prior": prior_map[ticker]["Weightings"],
                    "delta": delta,
                })

    return {"new": new_positions, "removed": removed, "changed": changed}

# ── Claude Summary ────────────────────────────────────────────────────────────

def build_context_prompt(today_data: dict, metrics: dict, diffs: dict) -> str:
    lines = [f"Date: {today_data['date']}\n"]
    for ticker in ETF_TICKERS:
        m = metrics[ticker]
        lines.append(f"## {ticker} — {ETF_NAMES[ticker]}")
        lines.append(f"AUM: ${m['net_assets']:,.0f} | Net Exposure: {m['net_exposure']:.1f}% | Gross Long: {m['gross_long']:.1f}% | Gross Short: {m['gross_short']:.1f}% | Cash: {m['cash_pct']:.1f}%")
        lines.append("Top 5 positions (by |weight|):")
        for h in m["top5"]:
            sign = "+" if h["Weightings"] >= 0 else ""
            lines.append(f"  {h['StockTicker']:20s} {sign}{h['Weightings']:.2f}%  {h['SecurityName']}")

        d = diffs.get(ticker, {})
        if d.get("new"):
            lines.append("New positions: " + ", ".join(f"{h['StockTicker']} ({h['Weightings']:+.1f}%)" for h in d["new"]))
        if d.get("removed"):
            lines.append("Removed: " + ", ".join(h["StockTicker"] for h in d["removed"]))
        if d.get("changed"):
            lines.append("Weight changes: " + ", ".join(f"{c['ticker']} {c['prior']:.1f}%→{c['today']:.1f}% ({c['delta']:+.1f}%)" for c in d["changed"]))
        lines.append("")

    return "\n".join(lines)


def snapshot_before_days(today: str, days: int) -> dict | None:
    """Return the snapshot closest to `days` calendar days before today, or oldest available."""
    cutoff = (datetime.strptime(today, "%Y-%m-%d") - timedelta(days=days)).strftime("%Y-%m-%d")
    files = sorted(CACHE_DIR.glob("*.json"))
    candidates = [f for f in files if f.stem < today]
    if not candidates:
        return None
    # Pick the file on or just before the cutoff; fall back to oldest if none reach that far
    before_cutoff = [f for f in candidates if f.stem <= cutoff]
    target = before_cutoff[-1] if before_cutoff else candidates[0]
    with open(target) as fh:
        return json.load(fh)


def build_gemma_context(today_data: dict, metrics: dict, window_days: int = 5) -> str:
    """Build context for Gemma using position changes over the last `window_days` days."""
    baseline = snapshot_before_days(today_data["date"], window_days)
    baseline_date = baseline["date"] if baseline else None

    rolling_diffs = {}
    if baseline:
        for ticker in ETF_TICKERS:
            rolling_diffs[ticker] = compute_diff(
                today_data["etfs"][ticker],
                baseline["etfs"].get(ticker, []),
            )

    lines = [f"Date: {today_data['date']}"]
    if baseline_date:
        lines.append(f"Position changes shown vs {baseline_date} ({window_days}-day window)\n")
    else:
        lines.append("")

    for ticker in ETF_TICKERS:
        m = metrics[ticker]
        lines.append(f"## {ticker} — {ETF_NAMES[ticker]}")
        lines.append(
            f"AUM: ${m['net_assets']:,.0f} | Net Exposure: {m['net_exposure']:.1f}% | "
            f"Gross Long: {m['gross_long']:.1f}% | Gross Short: {m['gross_short']:.1f}% | "
            f"Cash: {m['cash_pct']:.1f}%"
        )
        lines.append("Top 5 positions (by |weight|):")
        for h in m["top5"]:
            sign = "+" if h["Weightings"] >= 0 else ""
            lines.append(f"  {h['StockTicker']:20s} {sign}{h['Weightings']:.2f}%  {h['SecurityName']}")

        d = rolling_diffs.get(ticker, {})
        if d.get("new"):
            lines.append("Added over window: " + ", ".join(
                f"{h['StockTicker']} ({h['Weightings']:+.1f}%)" for h in d["new"]))
        if d.get("removed"):
            lines.append("Removed over window: " + ", ".join(h["StockTicker"] for h in d["removed"]))
        if d.get("changed"):
            lines.append("Net weight shifts: " + ", ".join(
                f"{c['ticker']} {c['prior']:.1f}%→{c['today']:.1f}% ({c['delta']:+.1f}%)"
                for c in d["changed"]))
        lines.append("")

    return "\n".join(lines)


